## A function to store and retrieve the inverted matrix of a given matrix and a function to calculate and display the inverted matrix from cache.

## Function that can be used to store and retrieve the inverted matrix of a given matrix in cache
makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinv <- function(inv) m <<- inv
        getinv <- function() m
        list(set = set, get = get,
             setinv = setinv,
             getinv = getinv)
}

## Function that takes a matrix as input and returns the inverted matrix. Uses inverted matrix from cache if available.
cacheSolve <- function(x, ...) {
        m <- x$getinv()
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- solve(data, ...)
        x$setinv(m)
        m
}

